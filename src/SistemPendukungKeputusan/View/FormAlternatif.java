    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtNama.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Silahkan Isi Nama Terlebih Dahulu !!");
            txtNama.requestFocus();
        }
        else{
            nama = txtNama.getText().trim();
            alamat = txtAlamat.getText().trim();
            kontak = txtKontak.getText().trim();
            ad.Save(nama, alamat, kontak);
            Refresh();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Konfirmasi", "Apa Anda Ingin Menghapus Data ?", JOptionPane.YES_NO_OPTION);
        if(ok==0){
            ad.Delete(ad.getId());
            Refresh();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        int rowSelected = jTable1.getSelectedRow();
        
        ad.setId(Integer.parseInt(jTable1.getValueAt(rowSelected, 0).toString()));
        txtNama.setText(jTable1.getValueAt(rowSelected, 1).toString());
        txtAlamat.setText(jTable1.getValueAt(rowSelected, 2).toString());
        txtKontak.setText(jTable1.getValueAt(rowSelected, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(txtNama.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Silahkan Isi Nama Terlebih Dahulu !!");
            txtNama.requestFocus();
        }
        else{
            Id = ad.getId();
            nama = txtNama.getText().trim();
            alamat = txtAlamat.getText().trim();
            kontak = txtKontak.getText().trim();
            ad.Update(Id, nama, alamat, kontak);
            Refresh();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void txtKontakKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKontakKeyTyped
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();

        if (!(((karakter >= '0') && (karakter <= '9') || (karakter == KeyEvent.VK_BACK_SPACE) || (karakter == KeyEvent.VK_DELETE) || (karakter == KeyEvent.VK_ENTER)))) {
            getToolkit().beep();
            evt.consume();
        }

        if (karakter == KeyEvent.VK_SPACE) {
            getToolkit().beep();
            evt.consume();
        }

        if (txtKontak.getText().length() >= 12) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtKontakKeyTyped

