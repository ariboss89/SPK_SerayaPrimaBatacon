    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtPassword.getText().equals("") || txtUsername.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Silahkan Isi Field !!");
        }
        else{
            con = new Koneksi();
            try{
                st = con.connect().createStatement();
                rs = st.executeQuery("SELECT *FROM tb_admin WHERE username = '"+txtUsername.getText().trim()+"' AND password = '"+txtPassword.getText().trim()+"'");
                if(rs.next()){
                    tb_admin.setRole(rs.getString("role"));
                    JOptionPane.showMessageDialog(null, "WELCOME");
                    
                    tb_admin.setKeepUsername(txtUsername.getText().trim());
                    new FormUtama().show();
                    dispose();
                }     
                else{
                    JOptionPane.showMessageDialog(null, "Password dan Username Anda Salah !!");
                }
            }catch(SQLException ex){
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

